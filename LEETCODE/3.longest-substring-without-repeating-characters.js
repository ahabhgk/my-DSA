// @lc code=start
/**
 * @param {string} s
 * @return {number}
 */
var lengthOfLongestSubstring = function(s) {
  const map = {}
  let res = 0
  let start = 0

  for (let i = 0; i < s.length; i++) {
    if (!map[s[i]]) map[s[i]] = -1

    if (map[s[i]] > start) {
      start = map[s[i]]
    }

    map[s[i]] = i
    res = Math.max(res, i - start)
  }

  return res
};
